package com.java.oopsprograms;

public class E {
	
	static byte a;
	static short b;
	static int c;
	static long d;
	static float e;
	static double f;
	static boolean g;
	static char h;
	
	
	public static void main(String[] args) {
		//printing the default values for the above static variables 
		
		System.out.println(E.a);//0
		System.out.println(E.b);//0
		System.out.println(E.c);//0
		System.out.println(E.d);//0
		System.out.println(E.e);//0.0
		System.out.println(E.f);//0.0
		System.out.println(E.g);//false
		System.out.println(E.h);

	}

}
