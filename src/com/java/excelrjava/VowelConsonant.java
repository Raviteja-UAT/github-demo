package com.java.excelrjava;

public class VowelConsonant {
    public static void main(String[] args) {
        char ch = 'z';
        // using if else condition
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch=='u')
            System.out.println("Vowel");
        else
            System.out.println("Consonant");
    }
}
