package com.java.excelrjava;

public class PositiveNegativeZero {

    public static void main(String[] args) {

        int x = 15;

        if (x > 0)
        {
            System.out.println("positive number");
        } else if (x < 0)
        {
            System.out.println("negative number");
        }
        else
        {
            System.out.println("zero");
        }
    }
}
